package model.ENTITY;

public class Disponivel {
	
	private String tipo, qtdedisp, qtdeloca, nome;

	public String getQtdeloca() {
		return qtdeloca;
	}

	public void setQtdeloca(String qtdeloca) {
		this.qtdeloca = qtdeloca;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getQtdedisp() {
		return qtdedisp;
	}

	public void setQtdedisp(String qtdedisp) {
		this.qtdedisp = qtdedisp;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	

}
